/**
 * Реестр правил («База правил», /rules) — ЧИСТЫЕ ДАННЫЕ, без БД и серверных
 * импортов: файл читают и промпт-фабрика (factory.ts), и клиентские компоненты
 * страницы /rules (рендер текстов и бейджей мест участия).
 *
 * Три вида записей:
 *  - SYSTEM_RULES — статические правила, чей текст живёт ЗДЕСЬ (в коде,
 *    обновляется с релизами); пользователь может только включать/выключать их
 *    (состояние — settings.rules_disabled, см. lib/rules.ts);
 *  - DYNAMIC_BLOCKS — метаданные программных блоков, чей текст собирается из
 *    данных конкретной группы (стартовый кадр, гардероб, локация…); тоже
 *    включаются/выключаются, но текст показать заранее нельзя;
 *  - пользовательские правила и правила из шаблонов живут в БД (prompt_rules,
 *    template_rules) — их реестр не описывает.
 */
