/**
 * Данные схемы «Как всё устроено» (/how-it-works).
 *
 * Схема — ОДИН путь производства серии (крупные узлы по центру):
 *   Сюжет → Разбивка на шоты → Промпт для видео → Готовое видео,
 * а вокруг — только то, что на этот путь влияет (спутники, сгруппированы
 * цветом по этапу, на который действуют). Описания — пользовательские:
 * что это, на что влияет и почему это важно; без имён файлов и функций.
 */

export type GraphGroup = { id: string; label: string; color: string };

export type GraphNode = {
  id: string;
  group: string;
  title: string;
  /** короткая подпись на карточке */
  sub?: string;
  /** базовая раскладка; пользовательские сдвиги хранятся в localStorage */
  x: number;
  y: number;
  /** размер карточки: этапы пути крупнее спутников */
  w: number;
  h: number;
  /** описание в панели деталей — «что, зачем и на что влияет» */
  details: string[];
};

export type GraphEdge = {
  from: string;
  to: string;
  label?: string;
  /** пунктир = влияние/обвязка; сплошная = основной путь серии */
  dashed?: boolean;
};

